#include "libft.h"

void	ft_putstr_ptf(char *s, int *len)
{
	int	i;

	i = 0;
	if (!s)
	{
		*len += write(1, "(null)", 6);
		return ;
	}
	while (s[i] != '\0')
	{
		*len += write(1, &s[i], 1);
		i++;
	}
}
